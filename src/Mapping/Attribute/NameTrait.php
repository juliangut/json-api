declare(strict_types=1);

namespace Jgut\JsonApi\Mapping\Attribute;

trait NameTrait
{
    protected ?string $name = null;

    public function __construct(?string $name = null)
    {
        $this->name = $name;
    }

    public function getName(): ?string
    {
        return $this->name;
    }
}
